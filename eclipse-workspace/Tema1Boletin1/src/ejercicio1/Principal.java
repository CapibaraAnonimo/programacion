package ejercicio1;

public class Principal {

	public static void main(String[] args) 
	{
		int num1 = 15;
		int num2 = 1;
		int num3 = 6;
		int res = 0;
		
		
		System.out.println("Hola usuario, este programa calcula el precio "
				+ "de multiplicar los 3 numeros introducidos y restarle 1.\n");
		
		res = (num1 + num2 + num3) - 1;
		System.out.println("El resultado es " + res + ".");
		
	}

}
